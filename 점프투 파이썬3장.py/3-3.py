#원래같으면 이리 써야하는것이다 먼저 설명하자면
a = [1, 2, 3, 4]
result = []
for num in a:
    result.append(num*3)
    print(result)
#a 라는 리스트를 만들고 리솔트라는 빈 리스트를 만든다
#엄버에다 a를 순서대로 집어넣은다음
