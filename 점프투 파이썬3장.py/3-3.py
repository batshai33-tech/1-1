#다섯명의 학생의 시험지를보고 점수를 계산해 합격과 불합격을 나눠 합격과 불합격을 출력해보자

marks = [90,20,57,73,39]

number = 0
for mark in marks:
    number = number + 1
    if mark >= 60:
        print('축하합니다 %d번 학생은 합격입니다' % number)
    else:
        print('아쉽개도 %d번 학생은 불합격입니다' % number)
#마크를 마크스에 순서대로 대입시킨다
#그리고 숫자가 1식 증가된다
#만약 마크가 60 이상이라면 합격을 축하하는 메시지를 넣고
#아니라면 불합격을 아쉬워하는 메시지를 출력한다

