#3, 6, 9, 12가 나오는걸 볼수잇다

#하지만 이를 리스트 컴프리헨션을 쓴다면?

a = [1, 2, 3, 4]
result = [num*3 for num in a]
print(result)
#리솔트는 넘버 곱하기 3이며
# 넘버에다 a를 순서대로 집어넣는다를 한줄로 간단히 쓸수있다

#만약 안에다가 다른조건을 넣어줄수도 있다 if문같은거 말이다
#Q: 만약 짝수에다가만 3을 곱하고싶다면?
a = [1, 2, 3, 4]
result = [num*3 for num in a if num % 2 == 0] #<- 넘버의 값을 2로 나누었을때 0이 되는것만 넘버 곱하기 3을 해줘서 출력한다
print(result)