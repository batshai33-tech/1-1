#리스트나 튜플 문자열의 첫번째 요소부터 마지막 요소까지 차례대로 변수에 대입되어
#수행할 문장 1,2,3,4,5... 등등 수행된다
#예제를 통해 더 깊개 알아보자

test_list = ['hello', 'pythun', 'world']
for a in test_list: #<- a 안에 태스트 리스트 안에 잇는 값을 순서대로 대입한다는 뜻
    print(a)

#리스트와 문자열로 서봤으니 더 다양하개 튜플로 다른것도 써보자
a_tuple = [(1,2),(3,4),(5,6)]
for (one, two) in a_tuple: #<- i 안에 에이 튜플 안에있는 리스트를 대입시킨다는 뜻
    print(one + two)
#방금과 같은 경우 리스트형식안 값의 형태가 튜플이였기 때문에 각각 요소가 자동으로 원 과 투 에 들어가진다

