# 이 함수(add) 를 썻을때 a + b 를 리턴하게 된다 라는것이다
# 그러니까 우리가 이제 일일이 a = 1 b = 2 c = a + b print(a + b).......
# 이 위에것을 쓸일없어 def 함수 하나로 이 위에것을 모두 단축해서 쓸수있다
# 함수이름()<= 이 옆에 들어갈때는 쓰는 곳에 따라 매개변수와 인수로 나눠진다
# (a, b) 이것이 매개변수다 함수에 입력으로 전달된 값을 받는 변수 를의미 (보통 def로 정의할때 씀)
# (3, 4) 이것이 인수 다 인수는 함수를 호출할때 전달하는 입력값을의미 (보통 print()로 출력할때 씀)

